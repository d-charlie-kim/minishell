#include "mijeong.h"
#include "parsing.h"

char	*get_env_value(t_env *env, const char *key)
{
	while (env)
	{
		if (!(ft_strncmp(env->key, key, ft_strlen(key) + 1)))
			return (env->value);
		env = env->next;
	}
	return (NULL);
}

void	print_error_and_exit(char *output, int error_num)
{
	write(STDERR_FILENO, output, ft_strlen(output) + 1);
	exit(error_num);
}
