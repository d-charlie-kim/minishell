#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	i;
	char	*s;
	void	*temp;

	i = 0;
	s = (char *)malloc(nmemb * size);
	if (!s)
		return (NULL);
	temp = (void *)s;
	while (i++ < nmemb * size)
		*s++ = 0;
	return (temp);
}
