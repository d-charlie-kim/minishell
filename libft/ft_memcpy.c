#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*n_dest;
	unsigned char	*n_src;

	n_dest = (unsigned char *)dest;
	n_src = (unsigned char *)src;
	if (!n_dest && !n_src)
		return (0);
	while (n--)
		*n_dest++ = *n_src++;
	return (dest);
}
